					<span>PH/NH3/NH4+</span>
					<span>簡稱為酸鹼值，PH  6 - 8 我們都可以稱為中性水質。 
所謂弱酸可以從6以下，弱鹼為8以上去定義比較好區別，買魚之前要務必需查清楚魚蝦喜歡的水質調整。 
PH其實也關係到NH3/NH4+ (氨/銨)的含量比例，NH不用想太複雜，想成魚會在水裡拉屎拉尿的汙染物。
在PH 7.0 以下大都轉成離子NH4+/銨，而在PH 7.0 以上剛好相反是容易轉為NH3/氨。
而我們現在只要先認識NH3是對魚蝦有質接影響的毒素，後面我們會還需要認識硝化系統和這之間的關係。
因PH越高就容易形成NH3氨比例增加，所以海水之類的弱鹼性飼養環境容易讓新手感覺好像不好養。
其實養水時間都是相同需要的，只是弱鹼性中因容易先累積的是NH3容易馬上致命所產生的誤會。
看到這裡，就稍微說一下，PH和硝化作用也是有關係。
硝化系統適合的環境是PH 6.5 ~ 8.5 ，而最佳平均在PH 7.8 。
PH 6.0 以下對硝化作用其實會影響效率，要特別注意喔！</span>
